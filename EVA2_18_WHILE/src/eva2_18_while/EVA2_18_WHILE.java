    public static void main(String[] args) {
   //while -> evaluan condiciones se ejecuta 0 o N veces
    // do while -> evaluan condiciones      se ejecuta  1 o N veces
    int i=1;
    while(i<=10){//se repite si la condicion es verdadera  
        System.out.println("MGVL");
        i++; //marca un limite //primero instruccion
    }   
    //simular un for con do while
    i=1;       
    do {
              System.out.println("don memo");
               i++; //instruccion 
        }while(i<=10);

    }
