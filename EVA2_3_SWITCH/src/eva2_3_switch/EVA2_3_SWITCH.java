public class EVA2_3_SWITCH {

   public static void main(String[] args) {
    String cade = "lunes";
    switch (cade) {
        case "lunes":
            System.out.println("Elegiste lunes");
            break;
        case "martes":
            System.out.println("Elegiste martes");
            break;
        case "miercoles":
            System.out.println("Elegiste miércoles");
            break;
        default:
            System.out.println("Día no válido");
            break;      
    }
}
   
     }
    
    

